      <P>The Nathan M. Newmark Civil Engineering Laboratory, or Newmark Lab, located at 205 N. Mathews Avenue in Urbana, Illinois on the campus of the University of Illinois at Urbanaâ€“Champaign, houses the university's Department of Civil and Environmental Engineering. The Lab was built in 1967, and has been modified and updated a number of times since then.[1] The facility was named after professor and department head Nathan M. Newmark after his death.

The building consists of classrooms and offices surrounding a large open area called the crane bay for large scale experiments,[1] including those of the Newmark Structural Engineering Lab (NSEL). Newmark also contains a professional machine shop where students and faculty can have material fabricated by the staffed professionals, and a student instrumentation workshop where students and faculty can fabricate materials themselves.The crane bay is a large open area in the center of Newmark Laboratory with a three-story height. It is where much of the department's structural research and testing is done. There are large doors on both the north and south sides of this area, where trucks and equipment can move through. The crane bay gets its name from two large yellow hydraulic cranes on rails above the room, which move heavy equipment around. The crane bay houses many structural analysis equipment, including a concrete cylinder crusher for strength tests and beam deflection measurement devices. There is also a 28-foot-tall (8.5 m) L-shaped concrete reaction wall with hydraulic actuators, which was added in 2004. The crane bay's large open size is also sometimes used for departmental events such as Engineering Open House and the department's annual career.
