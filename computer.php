      <P>A computer lab is a space where computer services are provided to a defined community. These are typically public libraries and academic institutions.[1] Generally, users must follow a certain user policy to retain access to the computers. This usually consists of rules such as no illegal activity during use or attempts to circumvent any security or content-control software while using the computers.[1]

Computer labs are often subject to time limits in order to allow more people access to use the lab. It is also common for personal login credentials to be required for access.[1] This allows institutions to track the user's activities for any possible fraudulent use. The computers in computer labs are typically equipped with internet access, scanners, and printers and are typically arranged in rows. This is to give the workstation a similar view to facilitate lecturing or presentations,[2] and also to facilitate small group work.[3]

For some academic institutions, student laptops or laptop carts [4] take place of dedicated computer labs. However, computer labs still have a place in applications requiring special software or hardware which are not easily accessible in personal computers</P>
